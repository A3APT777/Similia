// Переводим сегмент (часть рубрики между запятыми).
// Сначала проверяем целую фразу, потом — убывающие по длине n-граммы слов,
// затем — слово за словом.
function translateSegment(segment: string): string {
  const trimmed = segment.trim()
  const lower = trimmed.toLowerCase()

  // 1. Попытка перевести целый сегмент как фразу
  if (T[lower]) return T[lower]

  // 2. Ищем максимально длинные совпадающие фразы (жадный алгоритм слева направо)
  const words = trimmed.split(/\s+/)
  const result: string[] = []
  let i = 0

  while (i < words.length) {
    let matched = false
    // Пробуем от максимальной длины фразы до минимальной
    for (let len = Math.min(words.length - i, 4); len >= 2; len--) {
      const phrase = words.slice(i, i + len).join(' ').toLowerCase()
      if (T[phrase] !== undefined) {
        const translated = T[phrase]
        if (translated) result.push(translated)
        i += len
        matched = true
        break
      }
    }
    if (!matched) {
      // Переводим одно слово, убирая скобки и пунктуацию
      const raw = words[i]
      const leading = raw.match(/^[(\[{]+/)?.[0] || ''
      const trailing = raw.match(/[)\]}.,:;!?]+$/)?.[0] || ''
      const core = raw.slice(leading.length, raw.length - (trailing.length || 0))
      const w = core.toLowerCase()
      const translated = T[w]
      if (translated !== undefined) {
        if (translated) result.push(leading + translated + trailing)
      } else {
        result.push(raw) // оставляем оригинал
      }
      i++
    }
  }

  return result.join(' ').replace(/\s+/g, ' ').trim()
}

// Главная функция перевода рубрики для отображения.
// fullpath: "Mind, Anxiety, evening"  chapter: "Mind"
// возвращает: "Тревога, вечером"
export function translateRubric(fullpath: string, chapter: string): string {
  // Верхнеуровневая рубрика раздела (fullpath === chapter) — возвращаем перевод названия главы
  if (fullpath === chapter) {
    return CHAPTER_NAMES[chapter] || chapter
  }

  const prefix = chapter + ', '
  const withoutChapter = fullpath.startsWith(prefix)
    ? fullpath.slice(prefix.length)
    : fullpath

  return withoutChapter
    .split(', ')
    .map(seg => translateSegment(seg))
    .join(', ')
    .replace(/\s{2,}/g, ' ')
    .trim()
}
