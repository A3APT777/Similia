// Парсим top-5 из последнего full-flow прогона
const lastRun = readFileSync('/tmp/mdri-v6b-fullflow.txt', 'utf-8')
const norm = (s: string) => s.toLowerCase().replace(/\.$/, '')

function getTop5(caseNum: number): string[] {
  const regex = new RegExp(`#${caseNum}\\s.*?\\|\\s([\\w\\-\\.]+)\\(\\d+\\),\\s*([\\w\\-\\.]+)\\(\\d+\\),\\s*([\\w\\-\\.]+)\\(\\d+\\),\\s*([\\w\\-\\.]+)\\(\\d+\\),\\s*([\\w\\-\\.]+)\\(\\d+\\)`)
  const m = lastRun.match(regex)
  if (!m) return []
  return [m[1], m[2], m[3], m[4], m[5]]
}

async function main() {
  let beforeTop1 = 0, afterTop1 = 0
  let fixed = 0, broken = 0, unchanged = 0
  const details: string[] = []

  for (let i = 0; i < 50; i++) {
    const caseNum = i + 1
    const exp = EXPECTED[i]
    const top5 = getTop5(caseNum)

    if (top5.length === 0) {
      console.log(`#${caseNum}: SKIP (no top5 data)`)
      continue
    }

    const wasCorrect = norm(top5[0]) === exp
    if (wasCorrect) beforeTop1++

    const userMessage = `Текст пациента:\n"${TEXTS[i]}"\n\nКандидаты от реперторизации (в текущем порядке):\n${top5.map((r, j) => `${j + 1}. ${r}`).join('\n')}`

    try {
      const response = await client.messages.create({
        model: 'claude-sonnet-4-20250514',
        max_tokens: 800,
        temperature: 0.1,
        system: VERIFIER_PROMPT,
        messages: [{ role: 'user', content: userMessage }],
      })

      const text = response.content[0].type === 'text' ? response.content[0].text : ''
      const jsonStr = text.replace(/```json\s*/g, '').replace(/```\s*/g, '').trim()
      const newOrder = JSON.parse(jsonStr)
      const newTop1 = norm(newOrder[0]?.remedy ?? '')
      const isNowCorrect = newTop1 === exp

      if (isNowCorrect) afterTop1++

      if (!wasCorrect && isNowCorrect) {
        fixed++
        details.push(`  ✅ #${caseNum} FIXED: ${norm(top5[0])} → ${newTop1} (exp=${exp})`)
      } else if (wasCorrect && !isNowCorrect) {
        broken++
        details.push(`  ❌ #${caseNum} BROKEN: ${norm(top5[0])} → ${newTop1} (exp=${exp})`)
      } else {
        unchanged++
      }

      process.stdout.write('.')
    } catch (e: any) {
      console.log(`#${caseNum}: ERROR ${e.message?.substring(0, 100)}`)
      if (wasCorrect) afterTop1++ // сохраняем как было
      unchanged++
    }

    await new Promise(r => setTimeout(r, 300))
  }

  console.log('\n\n' + '='.repeat(60))
  console.log('ВЕРИФИКАТОР — РЕЗУЛЬТАТ НА 50 КЕЙСАХ')
  console.log('='.repeat(60))
  console.log(`До верификатора:    Top-1 = ${beforeTop1}/50 (${beforeTop1 * 2}%)`)
  console.log(`После верификатора: Top-1 = ${afterTop1}/50 (${afterTop1 * 2}%)`)
  console.log(`Дельта: ${afterTop1 - beforeTop1 >= 0 ? '+' : ''}${afterTop1 - beforeTop1}`)
  console.log()
  console.log(`Исправлено: ${fixed}`)
  console.log(`Сломано:    ${broken}`)
  console.log(`Без изменений: ${unchanged}`)
  console.log()
  if (details.length > 0) {
    console.log('Детали:')
    details.forEach(d => console.log(d))
  }
}

